CREATE OR REPLACE TABLE `lynclab-bigquery.abdel.table01` (
  DATES DATE,
  COUNT INT
)


AS (
  SELECT DATE(UTC_TIME) AS DATES, COUNT(MMSI) AS COUNT
  FROM `lynclab-bigquery.2024_01_KUKUP.00_TRAJID`
  WHERE UTC_TIME BETWEEN TIMESTAMP("2023-01-01 00:00:00") AND TIMESTAMP("2023-01-06 00:00:00")
  GROUP BY DATES
  ORDER BY DATES
);